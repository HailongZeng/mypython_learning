'''
软件编程的实质：就是将我们的思维转换成计算机能够识别语言的一个过程
面向过程：
1）自上而下顺序执行，逐步求精；
2）其程序结构是按照功能划分为若干基本模块
3）各模块之间的关系尽可能简单，在功能上相对独立
4）每一个模块内部均是由顺序、选择和循环三种基础结构
5）其模块化实现的具体方法是使用子程序
6）程序流程在写程序时就已经决定

面向对象：
1）把数据及对数据的操作方法放在一起，作为一个相互依赖的整体--对象
2）对同类对象抽象出其共性，形成类
3）类中的大多数数据，只能用本类的方法进行处理
4）类通过一个简单的外部接口与外界发生关系，对象与对象之间通过消息进行通信
5）程序流程由用户在使用中决定


面向对象是针对于面向过程而言的；
面向对象和面向过程都是一种思想；
面向过程：强调的是功能行为；关注的是解决问题需要哪些步骤
面向对象：将功能封装进对象，强调具备了功能的对象；关注的是解决问题需要哪些对象
面向对象是基于面向过程的。
'''